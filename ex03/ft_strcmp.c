int		ft_strcmp(char *s1, char *s2)
{
	int x = 0;
	int sum_one = 0;
	int sum_two = 0;
	while(s1[x] != '\0')
	{
		sum_one += s1[x];
		x++;
	}
	x = 0;
	while(s2[x] != '\0')
	{
		sum_two += s2[x];
		x++;
	}
	return sum_one - sum_two;
}
